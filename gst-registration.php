            <li class="list-group-item">
              <p class="content-info-text">
              1.GSTR - 1
GSTR - 1 is a return filed for outward supplies. This is a monthly return of a normal registered company under GST for their outward supplies. Can be filed on a quarterly basis for businesses with a turnover of less than 5 Crores in previous F.Y. This return shows the sale transactions for that particular month.
