Every person who is registered under GST is required to file foe GST return. This returns shows the details of 1) Invoices 2) Debit notes 3) Credit notes 4) Revised invoices showcasing the whole details of outward supply of your goods and services.
              </p>
              <p class="content-info-text">
              DUE DATE FOR FILING GSTR – 1
The standard time for filing GSTR – 1 is 10 days from the end of the month for which this return has to be filed. However this date can be extended for you to the 10th of the succeeding month by the commissioner only if the reasons are notified to them and valid.
               </p>
              <p class="content-info-text">
              2.GSTR – 2
GSTR – 2 is a return filed for inward supply of good and services as per the recipient request. It is monthly return filed by every GST registered firm. It contains the details of the purchase order by the recipient for that particular month. The details registered for this return is auto populated with details pf GSTR – 2A.
             </p>
             <p class="content-info-text">
             WHO NEEDS TO FILE GSTR – 2?
Every registered taxpayer under GST is required to file for this return. This return shows the details of purchase made by registered or unregistered taxpayers, debit and credit notes issued for inward purchase.
Further the recipient uses the details of GSTR – 2A and makes changes which are necessary.
            </p>
             <p class="content-info-text">
             DUE DATE FOR FILING GSTR – 2
The date for process of making changes and filing for return is the 11th and 15th day of succeeding month of which return is to be filed. Filing this return has not been enabled by the GSTN till now as it is under development stage.
              </p>
             <p class="content-info-text">
             3.	GSTR – 2A
GSTR – 2A is a read only document. This document is automatically filled after the supplier uploads his details in GSTR – 1. GSTR – 2A enables the recipient to check the details given by supplier. He can accept or reject the invoices which are pending in the details.
              </p>
              <p class="content-info-text">
              WHO NEEDS TO FILE FOR GSTR – 2A?
This return is to be filed by every taxpayer registered under GST. As it is a read only document that gets automatic filled from details given by supplier.
              </p>
              <p class="content-info-text">
              DUE DATE FOR FILING GSTR – 2A
Since GSTR – 2A is a read only document, any changes in details can be made by recipient in the form GSTR – 2 and then the changes are auto filled in GSTR – 2A form.
However the due date for making changes in form GSTR – 2 is mentioned above, so same goes here.
               </p>
              <p class="content-info-text">
              4.GSTR – 3B
It is a return filed for self-declaration showing summary details of inward and outward supply of goods and services. This return form can however cannot be revised. This form does not require to compare invoices of supplier and purchaser. Hence this form does not cause delay in filing as both supplier and purchaser are to be filed separately and there is no chance of late fees.
              </p>
              <p class="content-info-text">
              WHO NEEDS TO FILE FOR GSTR – 3B?
Every taxpayer who is registered under GST has to file for GSTR – 3B. Even when there are no inward and outward supply of goods and services, the taxpayer has to file nil return in this case. This return has also to be filed when taxpayer liability to pay tax is zero.
               </p>
              <p class="content-info-text">
              DUE DATE FOR FILING GSTR – 3B
This return has to be filed by the 20th of the succeeding month of the tax period in which GST is filed.
               </p>
               <p class="content-info-text">
               5.	GSTR – 4
This return is filed by every taxpayer registered under GST. It is a return filed for quarterly return by the company who has registered itself for composition schemes.
Under this scheme, the taxpayer which has an annual turnover of Rs. 1.5 crores needs to pay a fixed tax and quarterly return unlike the above taxpayers
               </p>
               <p class="content-info-text">
               WHO NEEDS TO FILE FOR GSTR – 4?
Every single person who has opted for composition schemes is likely to file for this quarterly return in GSTR – 4. It reduces the burden on small taxpayers.
             </p>
             <p class="content-info-text">
             For example if you want to file this return for quarter January – March 2020 then you must file it till 18th April, 2020.
               </p>
             <p class="content-info-text">
             6.	GSTR – 5
This is a monthly return filed by every non-resident taxpayer.
This return includes inward and outward supplies, interest or penalty fees, tax paid or has to pay, any other amount payable under act.
This return is only for non-resident taxpayer. Which also concludes that such taxpayer does not need to file any annual return.
               </p>
             <p class="content-info-text">
             WHO NEED TO FILE GSTR – 5?
A person who does not has any fixed place of business needs to file this return. A taxpayer who supplies goods and services occasionally. Such a person does not have a residence in India. Such a person can trade as an agent or as principal or any other.
               </p>
               <p class="content-info-text">
               DUE DATE FOR FILING GSTR – 5
This return has to be filed within 20 days after the end of calendar month and & 7 days after the last day of validation of certificate.
             </p>
               <p class="content-info-text">
               7.GSTR – 6 
This return has to be filed by taxpayer every month by an Input Service Distributor. This return filing gives a summary of the total input tax levied on taxpayer in that particular month.
As soon as this return is filed GSTR – 6A is auto drafted and it’s a read only document. This return contains details of the recipient in GSTR – 1.
             </p>
             <p class="content-info-text">
             DUE DATE FOR FILING GSTR – 6
GSTR – 6 is to be filed on 13th day of the month succeeding for which the tax is to be paid.
8.	GSTR – 7
This a monthly return filed by the taxpayers that are been deducted TDS under GST. This return contains details of tax deduced at source, liability for TDS, penalty or late fee if any.
Once the taxpayer fills the GSTR – 7 form details, GSTR – 7A form is automatically filled in the portal.
</p>
<p class="content-info-text">
DUE DATE FOR FILING GSTR – 7
This return needs to be filed within 10 days after the end of the month in which the deduction was made.
</p>
<p class="content-info-text">
9.GSTR – 8
This is a monthly return filed by every electronic commerce operator whose deduction includes tax collected at source under GST. This return contains details of the supplies which are made through e-commerce portal and tax is collected on supplies of goods and services.
</p>
<p class="content-info-text">
DUE DATE FOR FILING GSTR – 8
This return needs to be filed before or on 10th day of the month succession of the month where TCS is collected.
</p>
<p class="content-info-text">
10.	GSTR – 9
This return is filed by every normal taxpayer for annual return for every financial year. The person who is registered under GST but has no annual transaction also has to file for this return.
Every normal taxpayer requires to file this return except non-resident taxpayer, input service distributor, person paying tax under section 51 or 52.
</p>
<p class="content-info-text">
DUE DATE FOR FILING GSTR – 9
This return is to be filed on or before 31st day of December of every financial year. More adding to this rule, Rule 80(1) of CGST rules, such taxpayer must file its annual return online in form GSTR – 9.
</p>
<p class="content-info-text">
11.	GSTR – 9A
This return is to be files by every taxpayer who is a composition dealers every financial year. This return in addition to the quarterly return of composition dealers. This return contains details of the supplies of goods and services under composition schemes. It includes inward and outward supplies, tax paid, late fee any.
</p>
<p class="content-info-text">
12.	GSTR – 9B
Every e-commerce operator collecting tax at the source is necessary to file this return. One should avail its annual statement under section 52 in this form. This includes all the information by the operators in the monthly return filed during that financial year.
</p>
<p class="content-info-text">
DUE DATE FOR FILING GSTR – 9A
This return is to be filed on or before 31st December of financial year  i.e at the end of every financial year.
</p>
<p class="content-info-text">
13.	GSTR – 9C
Every person registered under GST whose aggregate turnover is more than Rs. 2 crores is mandatory for him to get his accounts audited by CA. Basically it is a reconcile statement giving value of supplies declared in annual return with audited annual accounts.
</p>
<p class="content-info-text">
DUE DATE FOR FILING GSTR – 9C
This due date for filing this return is same as that for GSTR – 9 i.e on or before 31st December of the subsequent financial year succeeding to the relevant FY under audit.
</p>
<p class="content-info-text">
14.	GSTR – 10
This is a return which has to be filed for registered person whose registration gets cancelled. This return excludes input service distributor, taxpayer coming under composition scheme, non-resident taxpayer, person collecting TDS or TCS.
</p>
<p class="content-info-text">
DUE DATE FOR FILING GSTR – 10
This return has to be filed by that particular person whose registration has been cancelled within a period of 3 months later from the date of cancellation or later on the date of cancellation of order.
</p>
<p class="content-info-text">
15.	GSTR – 11
This return has to be filed by taxpayer who is allotted with Unique Identification Number (UIN). UIN is issued to a registered person so that he can claim refunds for GST paid on goods and services purchased in India.
</p>
<p class="content-info-text">
WHO CAN APPLY FOR UNIQUE IDENTIFICATION NUMBER (UIN)?
UIN is only allotted to foreign embassies and diplomatic missions who are not liable to pay tax in India. This unique number is given to them so that can claim refund GST for tax paid to Indian authorities. The organizations who can apply for this number are some specialized agency of UN authority, embassy of foreign country, financial institution coming under UN act, 1947.
</p>
<p class="content-info-text">
DUE DATE FOR FILING GSTR – 11
The due date for filing this return is 28th of the month subsequent to the month in which inward supplies are been received by UIN holders. This return is not filed monthly, but are filed on the basis of supplies made.
</p>
