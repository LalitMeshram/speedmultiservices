          <p class="content-info-text">1.GENERAL INFORMATION
This is the start of the report which gives overview of the project. It must include past experience, present status and future prospects of the field. It should also include information about products manufactured, process of manufacturing, Reason for selecting that particular product.
It should clarify other alternatives in business and reason for starting the business.
</p>
          <p class="content-info-text">2.EXECUTIVE SUMMARY
In this part of report, one must state the objective of the business and all the procedures through which the business will be carried out. It must also include the assumptions and risk involved in starting the business.
</p>
          <p class="content-info-text">3.ORGANIZATION SUMMARY
This part of the report should include structure of organization and pattern of the unit. It must also show whether the ownership is sole proprietorship, partnership or Joint stock company. It should also include all the bio-data of the owners, financial promoters, name, age, address.
</p>
          <p class="content-info-text">4.PROJECT DESCRIPTION
In this part a brief description of the project must be included about:
•	Location of the site
•	Raw material required
•	Target of production
•	Area required for setting up of company
•	Power requirements
•	Fuel requirements
•	Water requirements
•	Employment requirements of skilled and unskilled labour
•	Technology used for production
•	Procedure of production
•	Requirement of pollution treatment plants
•	It should also include types of services given to customers and by which method.
</p>
          <p class="content-info-text">5.MARKETING PLAN
This part must clearly include total expected demand for the product. It should also contain the maximum retail price through which the product is to be sold in the market. It should also showcase the strategies of advertisement and publicity. It should tell the readers the mode of distribution from unit to market. It must state
•	Types of customers
•	Future prospects of market
•	Marketing cost of the product
•	Manufacturing cost of the product
•	Demand of the product in national, global market
</p>
          <p class="content-info-text">6.CAPITAL STRUCTURE AND OPERATING COST
This should include the total capital requirements in the project. It should also describe the source of finance. Proposed financial structure of venture should indicate the expected sources and terms of equity and debt financing.
</p>
<p class="content-info-text">7.MANAGEMENT PLAN
This part must contain business experience of the promoters or owners, Details of members under team management, duties and responsibilities of team members, Current needs of the organization, Methods through which business will be managed, Plans for training and appointing employees, Programmes and policies of the management.
</p>
<p class="content-info-text">8.FINANCIAL ASPECTS
A detailed balance sheet must be included in this part so that how much profit company tends to earn can be explained in better way. It should show net profit and loss, sales revenue, cost of production. This statement must show the cash flow for consecutive 3 to 5 years and each individual year. The accounting and inventory control system used foe company is shown here. It should also state whether the business is financially and economically possible.
</p>
<p class="content-info-text">9.TECHNICAL ASPECTS
It includes all the technical details of machines used for process of product making. It includes technology used for production of product, machinery capacity, pollution controlling plants, etc.
</p>
<p class="content-info-text">10.PROJECT IMPLEMENTATION
This must contain a fixed time period which will take for the completion of the entire project. 
</p>
<p class="content-info-text">11.SOCIAL RESPONSIBILITY
This part shows others the inputs drawn from the society. Hence the project’s contribution to the society in the form of income, exports and imports, infrastructure. It should also indicate output of the business.
</p>
